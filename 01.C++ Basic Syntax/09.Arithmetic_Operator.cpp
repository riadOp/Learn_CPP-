#include<iostream>

int main(){
    int student =20;
    
    student = student + 2;//Addition!! or student+=2 or Student++;
    student = student -2 ;//Subtraction !! student-=2 or student--; 
    student = student * 2;//Multiplication!! or student*=2;
    student = student /2;//Division!! or student /=2;
    student = student %2;//Modulas!! or student%=2;

    std::cout << student <<'\n';
    
    return 0;
}